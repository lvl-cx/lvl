LVLConfig = {} -- Global variable for easy referencing. 

LVLConfig.MoneyUiEnabled = true; -- Set to false to disable Money in the top right corner. 
LVLConfig.SurvivalUiEnabled = true; -- Controls the UI under the healthbar.
LVLConfig.EnableComa = true; -- Controls the LVL coma on death.
LVLConfig.EnableFoodAndWater = true; -- Controls the food and water system.
LVLConfig.EnableHealthRegen = true; -- Controls the health regen. (Whether they regen health after taking damage do not disable if coma is enabled.)
LVLConfig.EnableBuyVehicles = true; -- Enables ability to buy vehicles from the RageUI Garages.  
LVLConfig.LoadPreviews = true; -- Controls the car previews with the RageUI Garages.
LVLConfig.VehicleStoreRadius = 250; -- Controls radius a vehicle can be stored from.
LVLConfig.AdminCoolDown = false; -- Enables an admin cooldown on call admin.
LVLConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
LVLConfig.StoreWeaponsOnDeath = true; -- Stores the players weapon on death allowing them to be looted.
LVLConfig.DoNotDisplayIps = true; -- Removes all LVL related references in the console to player ip addresses.
LVLConfig.LoseItemsOnDeath = true; -- Controls whether you lose inventory items on death.
LVLConfig.AllowMoreThenOneCar = false; -- Controls if you can have more than one car out.
LVLConfig.F10System = true; -- Logs warnings and can be accessed via F10 (Thanks to Rubbertoe98) (https://github.com/rubbertoe98/FiveM-Scripts/tree/master/lvl_punishments)
LVLConfig.ServerName = "LVL" -- Controls the name that is displayed on warnings title etc.
LVLConfig.PlayerSavingTime = 3000 -- Time in milliseconds to update Player saving
---------------
LVLConfig.LootBags = true; -- Enables loot bags and disables looting. 
LVLConfig.DisplayNamelLootbag = false; -- Enables notification of who's lootbag you have opened
